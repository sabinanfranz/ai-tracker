def _connect() -> sqlite3.Connection:
    conn = sqlite3.connect(str(DB_PATH))
    conn.row_factory = sqlite3.Row
    return conn


def cmd_generate(args: argparse.Namespace) -> None:
    conn = _connect()
    where = "1=1" if args.force else "title_updated_ko IS NULL"
    sql = f"SELECT id, event_date, title, content, title_updated, content_updated FROM chatgpt_event WHERE {where} ORDER BY event_date DESC"
    if args.limit > 0:
        sql += f" LIMIT {args.limit}"
    rows = [dict(r) for r in conn.execute(sql).fetchall()]
    conn.close()
    out = open(sys.stdout.fileno(), "w", encoding="utf-8", closefd=False)
    json.dump(rows, out, ensure_ascii=False, indent=2)
    out.flush()
    print(file=sys.stderr)
    print(f"[enrich_chatgpt_ko] {len(rows)} row(s) pending", file=sys.stderr)


def cmd_apply(args: argparse.Namespace) -> None:
    path = Path(args.file)
    if not path.exists():
        print(f"[error] file not found: {path}", file=sys.stderr)
        sys.exit(1)
    data: list[dict] = json.loads(path.read_text(encoding="utf-8"))
    conn = _connect()
    updated = 0
    for item in data:
        rid = item.get("id")
        title_ko = item.get("title_updated_ko")
        content_ko = item.get("content_updated_ko")
        if not rid or not title_ko or not content_ko:
            print(f"[skip] incomplete: {rid}", file=sys.stderr)
            continue
        conn.execute(
            "UPDATE chatgpt_event SET title_updated_ko = ?, content_updated_ko = ?, updated_at = datetime('now') WHERE id = ?",
            (title_ko, content_ko, rid),
        )
        updated += 1
    conn.commit()
    conn.close()
    print(f"[enrich_chatgpt_ko] applied {updated}/{len(data)} row(s)")


def cmd_seed(_args: argparse.Namespace) -> None:
    conn = sqlite3.connect(str(DB_PATH))
    cur = conn.cursor()
    for record_id, title_ko, content_ko in RECORDS:
        cur.execute(
            "UPDATE chatgpt_event SET title_updated_ko = ?, content_updated_ko = ? WHERE id = ?",
            (title_ko, content_ko, record_id),
        )
    conn.commit()
    updated = cur.execute(
        "SELECT COUNT(*) FROM chatgpt_event WHERE title_updated_ko IS NOT NULL"
    ).fetchone()[0]
    conn.close()
    print(f"[enrich_chatgpt_ko] seed: {updated} records")


def cmd_status(_args: argparse.Namespace) -> None:
    conn = _connect()
    total = conn.execute("SELECT COUNT(*) FROM chatgpt_event").fetchone()[0]
    done = conn.execute("SELECT COUNT(*) FROM chatgpt_event WHERE title_updated_ko IS NOT NULL").fetchone()[0]
    conn.close()
    print(f"[enrich_chatgpt_ko] total={total}  done={done}  pending={total - done}")


def main() -> None:
    parser = argparse.ArgumentParser(description="ChatGPT event Korean enrichment")
    sub = parser.add_subparsers(dest="command")

    p_gen = sub.add_parser("generate", help="Output pending rows as JSON")
    p_gen.add_argument("--limit", type=int, default=0)
    p_gen.add_argument("--force", action="store_true")

    p_apply = sub.add_parser("apply", help="Apply enrichment JSON to DB")
    p_apply.add_argument("file", help="Path to enrichment JSON file")

    sub.add_parser("seed", help="Apply hardcoded historical data")
    sub.add_parser("status", help="Show enrichment progress")

    args = parser.parse_args()
    cmd = args.command
    if cmd == "generate":
        cmd_generate(args)
    elif cmd == "apply":
        cmd_apply(args)
    elif cmd == "seed":
        cmd_seed(args)
    elif cmd == "status":
        cmd_status(args)
    else:
        parser.print_help()


if __name__ == "__main__":
    main()
